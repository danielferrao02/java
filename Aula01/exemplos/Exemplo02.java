package exemplos;

public class Exemplo02 {
    public static void main(String[] args){
        // Operadores aritméticos (matemáticos) : + - * / 

        System.out.println(10 + 5); // trata-se de uma soma
        System.out.println(10 - 5);
        System.out.println(10 * 5);
        System.out.println(10 / 5);

        System.out.println("O resultado é " + (10 + 5)); // concatenação entre texto e operacao aritmetica
        System.out.println("O resultado é " + 10 * 5);
    }
}
